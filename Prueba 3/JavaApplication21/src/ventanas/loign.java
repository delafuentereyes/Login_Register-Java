    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String usuario = txtUsuario.getText();
        String pass = String.valueOf(txtpassContra.getPassword());

        boolean inicio = true;

        for (datos i : array.personas) {
            if (i.getUsuario().equals(usuario) && i.getContra().equals(pass)) {
                inicio = true;
            } else {
                inicio = false;
            }
        }
        
        if (inicio) {
            tareas t = new tareas();
            t.setVisible(true);
            dispose();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Usuario inexistente. Verifique su usuario o contrase침a");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

